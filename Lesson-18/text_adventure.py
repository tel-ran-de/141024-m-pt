#     - Выбор:
#         - Использовать ключ для выхода
#         - Оставить ключ и искать дальше
# 9. Использовать ключ для выхода:
# Вы находите дверь, открываете ее ключом и выходите из замка. Вы выиграли!
# 10. Правая дверь:
